#include "libft.h"
#include <stdlib.h>

t_list		*ft_lstnew(void const *content, t_size content_size)
{
	t_list	*ptr;

	ptr = (t_list *)malloc(sizeof(t_list));
	if (!ptr)
		return (NULL);
	ptr->content = content;
	if (ptr->content)
		ptr->content_size = content_size;
	else
		ptr->content_size = 0;
	ptr->next = NULL;
	return (ptr);
}
