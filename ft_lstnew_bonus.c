#include "libft.h"
#include <stdlib.h>

t_list	*ft_lstnew(void *content)
{
	t_list	*ptr;

	ptr = (t_list *)malloc(sizeof(t_list));
	if (!ptr)
		return (LIBFT_NULL);
	ptr->content = content;
	ptr->next = LIBFT_NULL;
	return (ptr);
}
