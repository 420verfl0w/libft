#include "libft.h"

int	ft_strncmp(char *s1, char *s2, t_size n)
{
	if (!n)
		return (0);
	while (*s1 && *s2 && --n && *s1++ == *s2)
		s2++;
	return ((unsigned char)*s1 - (unsigned char)*s2);
}
