#include "libft.h"

char	*ft_strncpy(char *dest, char *src, t_size n)
{
	char	*tmp;

	tmp = dest;
	n++;
	while (--n && *src)
		*tmp++ = *src++;
	while (n--)
		*tmp++ = 0;
	return (dest);
}
