#include "libft.h"

char	*ft_strrev(char *str)
{
	int	i;
	int	x;

	i = 0;
	x = ft_strlen(str) - 1;
	while (i < x)
	{
		str[i] ^= str[x];
		str[x] ^= str[i];
		str[i++] ^= str[x--];
	}
	return (str);
}
